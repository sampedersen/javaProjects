/*

Write a program that takes in an input of four doubles and outputs an average of those four numbers rounded to the nearest whole number.
For example, if the input is: "5.0 6.0 7.0 8.0" then the output is: "7.0"

*/
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////            Program             /////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import java.util.Scanner;

public class integerAveraging {
   public static void main(String[] args) {
      double average;
      
      // get the four numbers from user
      Scanner scnr = new Scanner(System.in);
      double num1 = scnr.nextDouble();
      double num2 = scnr.nextDouble();
      double num3 = scnr.nextDouble();
      double num4 = scnr.nextDouble();   
  
     // calculations
      average = Math.round((num1 + num2 + num3 + num4)/4.0);
      
      // print the results. 
      System.out.print(average);

   }
}
