/*

Write a program that takes in a value for the radius of a circle, and outputs the area and circumference of that circle 
rounded to two decimal places (the rounding has been done for you).
For example, if the input is: "5.0" then the output is: 
"The area of this circle is: 78.50
The circumference of this circle is: 31.40"

*/
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////            Program             /////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import java.util.Scanner;

public class circleCalculations {
   public static void main(String[] args) {
      double pi = 3.14;
      double radius;
      double area;
      double circumference;
      
      // get the radius from user
      Scanner scnr = new Scanner(System.in);
      radius = scnr.nextDouble();
      
      // calculate area and circumference 
      area = radius*radius*pi;
      circumference = radius*pi*2.0;

      // print the results. 
      System.out.println("The area of this circle is: " +  String.format("%.2f", area));
      System.out.println("The circumference of this circle is: " +  String.format("%.2f", circumference));

   }
}
