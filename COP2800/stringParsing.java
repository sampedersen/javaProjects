/*

Write a program that takes in a string as input and output's the number of time the letter 'a' is in the string. 
For example, if the input is: "banana" then the output is: "3" 

*/
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////            Program             /////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import java.util.Scanner;

public class stringParsing {
   public static void main(String[] args) {
      Scanner scnr = new Scanner(System.in);
      String str = scnr.nextLine();
      int count = 0;
      int i;
      int length;
      
      length = str.length();
      
      for (i = 0; i < length; ++i) {
         if (str.charAt(i) == 'a') {
            count += 1;
         }
      }
      System.out.print(count);
      
      //write your code here
      
   }
}
