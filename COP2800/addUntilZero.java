/*

Write a program that takes in an unknown number of integers until a 0 is inputted, and adds up all the numbers inputted. 
For example, if the input is: "5 3 8 -2 0" then the output is: "14"

*/
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////            Program             /////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


import java.util.Scanner;

public class addUntilZero {
   public static void main(String[] args) {
      Scanner scnr = new Scanner(System.in);
      int input; 
      int sum = 0;
      
      input = scnr.nextInt();
      while (input != 0) {
         sum = sum + input; 
         input = scnr.nextInt();
      }
      System.out.print(sum);
      
      
   }
}
