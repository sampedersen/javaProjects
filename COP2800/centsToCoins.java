/*

Write a program that takes in a number of cents and returns the number of quarters, dimes, nickels, and pennies that 
would be needed to make that amount using the smallest number of coins possible.
Example, if the input is: "86" then the output is: "3 1 0 1"

*/
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////            Program             /////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import java.util.Scanner;

public class centsToCoins {
   public static void main(String[] args) {
      int quarters;
      int dimes;
      int nickels;
      int pennies;
      
      // get the amount from user
      Scanner scanner = new Scanner(System.in);
      int money = scanner.nextInt();
      
      // calculate number of quarters
      quarters = money/25;
      
      // calculate number of dimes
      dimes = (money - quarters*25)/10;
      
      // calculate number of nickels
      nickels = (money - (quarters*25) - (dimes*10))/5;
      
      // calculate number of pennies
      pennies = (money - (quarters*25) - (dimes*10) - (nickels*5))/1;
      
      // print the results
      System.out.print(quarters + " " + dimes + " " + nickels + " " + pennies);

   }
}
