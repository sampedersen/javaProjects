/*

Write a program that reads in hours, minutes and seconds as input, and outputs the time in seconds only.
Example: if the input is: "1 6 40", where 1 is the number of hours, 6 is the number of minutes, and 40 is the number of seconds, the output is:
"Seconds: 4000"

*/
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////            Program             /////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import java.util.Scanner;

public class convertToSec {
   public static void main(String[] args) {
      int seconds;
      int minutes;
      int hours;
     
      Scanner scanner = new Scanner(System.in);
      
      hours = scanner.nextInt(); 
      minutes = scanner.nextInt();
      seconds = scanner.nextInt();
      int totalSeconds = hours*3600 + minutes*60 + seconds;
      System.out.println("Seconds: " + totalSeconds);
      
   }
}
